# Ejercicio 5: Manejo de matrículas en una tupla

matriculas = (101, 102, 103, 104, 105)

matricula_buscada = 102
matricula_buscada = matriculas.count(102)
print(f"La matrícula 102 aparece {matricula_buscada} veces en la tupla")

matricula_posicion = 104
matricula_posicion = matriculas.index(104)
print(f"La matrícula 104 esta en la posición {matricula_posicion}")
